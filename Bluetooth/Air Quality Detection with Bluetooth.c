BluetoothSerial SerialBT;

void setup() {
   Serial.begin(115200);
   SerialBT.begin("CORECHAMP IOT Home Kit"); //Bluetooth device name
   Serial.println("The device started, now you can pair it with bluetooth!");

   // SSD1306_SWITCHCAPVCC = generate display voltage from 3.3V internally
  if(!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) {
    Serial.println(F("SSD1306 allocation failed"));
    for(;;); // Don't proceed, loop forever
  }
  delay(2000); // Pause for 2 seconds
 
  // Clear the buffer.
  display.clearDisplay();
  
  // Draw bitmap on the screen
  display.drawBitmap(0, 0, image_data_20190410_114707, 128, 64, 1);
  display.display();
  delay(2000);
  display.clearDisplay();
  display.setTextColor(WHITE);
}

void loop() {
  sensorvalue = analogRead(0);
  Serial.println("Air Quality:");
  Serial.println(sensorvalue);
  SerialBT.print("Air Quality:");
  SerialBT.println(sensorvalue);
 
  display.clearDisplay();
  display.setCursor(10,1);
  display.setTextSize(2);
  display.print("CORECHAMP");
  delay(100);
  display.setCursor(0,55);
  display.setTextSize(1);
  display.print("SENSOR VALUE: "); 
  delay(100);
  display.print(sensorvalue);
  display.print("ppm");
  delay(100);
  
  if(sensorvalue < MQ135_THRESHOLD){
      Serial.println("Good air quality");
      SerialBT.println("Good air quality");
      display.setCursor(15,30);
      display.setTextSize(1);
      display.print("Good air quality");
      }
      else {
      Serial.println("Poor air quality");
      SerialBT.println("Poor air quality");
      display.setCursor(15,30);
      display.setTextSize(1);
      display.print("Poor air quality");
      }
  
  display.display();

  delay(2000);
}
